    {
        string ret;
        int n=count.size();
        int i,j,k,l;
        for(i=0;i<count.size();i++)
        {
            a[i].pos=position[i];
            a[i].cnt=count[i];
        }
        sort(a,a+n,comp);
        mem(flag,0);

        int low=-inf;
        int high=inf;

        for(i=0;i<n;i++)
        {
            low=a[i].pos-time+z;
            high=a[i].pos+time+z;
            int cnt=0;
            while(low<=high && cnt<a[i].cnt)
            {
                if(flag[low]==0)
                {
                    cnt++;
                    flag[low]=1;
                }
                low++;
            }
            if(cnt<a[i].cnt)
                return "Impossible";
        }

        return "Possible";
    }

